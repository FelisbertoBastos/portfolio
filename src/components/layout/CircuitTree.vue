  name: 'CircuitTree',
  mounted () {
    $(circuitAnimation)

    function circuitAnimation () {
      const paths = $('#Paths').children()
      const vPaths = document.getElementsByClassName('cls-1')

      paths.each(function (index) {
        const pathLength = vPaths[index].getTotalLength()

        const innerCircuitAnimation = () => {
          $(this)
            .queue('steps', function (next) {
              $(this).children('g')
                .animate({
                  'opacity': 1
                }, 1000, next)
            })
            .queue('steps', function (next) {
              $(this).children('path')
                .css({
                  'opacity': 1,
                  'stroke-dasharray': pathLength,
                  'stroke-dashoffset': pathLength
                })
              next()
            })
            .queue('steps', function (next) {
              $(this).children('path')
                .animate({
                  'stroke-dashoffset': 0
                }, pathLength * 10, next)
            })
            .queue('steps', function (next) {
              $(this).children('path')
                .animate({
                  'opacity': 0
                }, 1000, innerCircuitAnimation)
            })
            .dequeue('steps')
        }

        innerCircuitAnimation()
      })
    }
  }
}
